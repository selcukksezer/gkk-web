import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const planPath = path.join(root, "PLAN_01_ITEMS_EQUIPMENT.md");
const outDir = path.join(root, "supabase", "seeds");
const outPath = path.join(outDir, "plan1_items.generated.sql");

const rarityMinLevel = {
  common: 1,
  uncommon: 5,
  rare: 15,
  epic: 25,
  legendary: 40,
  mythic: 55,
};

const slotMap = {
  wpn: "weapon",
  chest: "chest",
  head: "head",
  legs: "legs",
  boots: "boots",
  gloves: "gloves",
  ring: "ring",
  neck: "necklace",
};

const weaponSubtypes = new Set(["dagger", "sword", "axe", "staff"]);
const armorSubtypes = new Set(["plate", "chain", "leather", "robe"]);

function toInt(text) {
  const n = String(text || "")
    .replace(/[.,]/g, "")
    .replace(/[^0-9-]/g, "");
  return n ? Number.parseInt(n, 10) : 0;
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function detectSlot(itemId) {
  const prefix = itemId.split("_")[0];
  return slotMap[prefix] || "none";
}

function detectSubtype(itemId) {
  const parts = itemId.split("_");
  return parts[1] || "none";
}

function buildRow(row) {
  const slot = detectSlot(row.item_id);
  const subtype = detectSubtype(row.item_id);
  const itemType = slot === "weapon" ? "weapon" : "armor";
  const weaponType = slot === "weapon" && weaponSubtypes.has(subtype) ? subtype : "none";
  const armorType = armorSubtypes.has(subtype) ? subtype : "none";
  const power = Math.round(row.attack + row.defense + row.health / 10 + row.luck * 2);
  const basePrice = Math.max(100, Math.round(power * 1.4));
  const vendorSellPrice = Math.max(10, Math.round(basePrice * 0.35));

  return {
    id: row.item_id,
    name: row.name,
    description: `${row.name} (${row.rarity})`,
    icon: row.item_id,
    item_type: itemType,
    rarity: row.rarity,
    equip_slot: slot,
    weapon_type: weaponType,
    armor_type: armorType,
    sub_type: subtype,
    attack: row.attack,
    defense: row.defense,
    health: row.health,
    power,
    luck: row.luck,
    required_level: rarityMinLevel[row.rarity] ?? 1,
    can_enhance: true,
    max_enhancement: 10,
    is_han_only: false,
    is_market_tradeable: true,
    is_direct_tradeable: true,
    base_price: basePrice,
    vendor_sell_price: vendorSellPrice,
    is_stackable: false,
    max_stack: 1,
    is_tradeable: true,
  };
}

function parsePlanRows(markdown) {
  const lines = markdown.split(/\r?\n/);
  const rows = [];

  for (const line of lines) {
    if (!line.startsWith("|")) continue;
    if (!line.includes("`")) continue;

    const cols = line
      .split("|")
      .map((c) => c.trim())
      .filter(Boolean);

    // Expected table row: Rarity | Item ID | Name | Attack | Defense | HP | Luck
    if (cols.length < 7) continue;

    const rarity = cols[0].toLowerCase();
    const itemIdRaw = cols[1];
    if (!itemIdRaw.startsWith("`") || !itemIdRaw.endsWith("`")) continue;

    const itemId = itemIdRaw.slice(1, -1);
    const name = cols[2];
    const attack = toInt(cols[3]);
    const defense = toInt(cols[4]);
    const health = toInt(cols[5]);
    const luck = toInt(cols[6]);

    rows.push({ rarity, item_id: itemId, name, attack, defense, health, luck });
  }

  return rows;
}

function toSql(rows) {
  const cols = [
    "id",
    "name",
    "description",
    "icon",
    "item_type",
    "rarity",
    "equip_slot",
    "weapon_type",
    "armor_type",
    "sub_type",
    "attack",
    "defense",
    "health",
    "power",
    "luck",
    "required_level",
    "can_enhance",
    "max_enhancement",
    "is_han_only",
    "is_market_tradeable",
    "is_direct_tradeable",
    "base_price",
    "vendor_sell_price",
    "is_stackable",
    "max_stack",
    "is_tradeable",
  ];

  const valuesSql = rows
    .map((r) =>
      `(${[
        sqlString(r.id),
        sqlString(r.name),
        sqlString(r.description),
        sqlString(r.icon),
        sqlString(r.item_type),
        sqlString(r.rarity),
        sqlString(r.equip_slot),
        sqlString(r.weapon_type),
        sqlString(r.armor_type),
        sqlString(r.sub_type),
        r.attack,
        r.defense,
        r.health,
        r.power,
        r.luck,
        r.required_level,
        r.can_enhance,
        r.max_enhancement,
        r.is_han_only,
        r.is_market_tradeable,
        r.is_direct_tradeable,
        r.base_price,
        r.vendor_sell_price,
        r.is_stackable,
        r.max_stack,
        r.is_tradeable,
      ].join(", ")})`
    )
    .join(",\n");

  return `-- AUTO-GENERATED from PLAN_01_ITEMS_EQUIPMENT.md\n-- Generated by scripts/generate-plan1-item-seed.mjs\n\nINSERT INTO public.items (${cols.join(", ")})\nVALUES\n${valuesSql}\nON CONFLICT (id) DO UPDATE SET\n  name = EXCLUDED.name,\n  description = EXCLUDED.description,\n  icon = EXCLUDED.icon,\n  item_type = EXCLUDED.item_type,\n  rarity = EXCLUDED.rarity,\n  equip_slot = EXCLUDED.equip_slot,\n  weapon_type = EXCLUDED.weapon_type,\n  armor_type = EXCLUDED.armor_type,\n  sub_type = EXCLUDED.sub_type,\n  attack = EXCLUDED.attack,\n  defense = EXCLUDED.defense,\n  health = EXCLUDED.health,\n  power = EXCLUDED.power,\n  luck = EXCLUDED.luck,\n  required_level = EXCLUDED.required_level,\n  can_enhance = EXCLUDED.can_enhance,\n  max_enhancement = EXCLUDED.max_enhancement,\n  is_han_only = EXCLUDED.is_han_only,\n  is_market_tradeable = EXCLUDED.is_market_tradeable,\n  is_direct_tradeable = EXCLUDED.is_direct_tradeable,\n  base_price = EXCLUDED.base_price,\n  vendor_sell_price = EXCLUDED.vendor_sell_price,\n  is_stackable = EXCLUDED.is_stackable,\n  max_stack = EXCLUDED.max_stack,\n  is_tradeable = EXCLUDED.is_tradeable;\n`;
}

function main() {
  if (!fs.existsSync(planPath)) {
    throw new Error(`PLAN file not found: ${planPath}`);
  }

  const markdown = fs.readFileSync(planPath, "utf8");
  const parsed = parsePlanRows(markdown);
  const equipmentRows = parsed
    .filter((r) => {
      const slot = detectSlot(r.item_id);
      return slot !== "none";
    })
    .map(buildRow);

  if (equipmentRows.length === 0) {
    throw new Error("No PLAN 01 equipment rows parsed.");
  }

  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(outPath, toSql(equipmentRows), "utf8");

  console.log(`Generated ${equipmentRows.length} equipment rows -> ${path.relative(root, outPath)}`);
}

main();
