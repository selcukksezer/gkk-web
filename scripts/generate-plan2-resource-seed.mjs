import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const planPath = path.join(root, "PLAN_02_FACILITIES_RESOURCES.md");
const outDir = path.join(root, "supabase", "seeds");
const outPath = path.join(outDir, "plan2_resources.generated.sql");

const facilityIcons = {
  mining: "⛏️",
  quarry: "🪨",
  lumber_mill: "🪵",
  clay_pit: "🏺",
  sand_quarry: "🏜️",
  farming: "🌾",
  herb_garden: "🌿",
  ranch: "🐄",
  apiary: "🐝",
  mushroom_farm: "🍄",
  rune_mine: "🔮",
  holy_spring: "💧",
  shadow_pit: "🌑",
  elemental_forge: "🔥",
  time_well: "⏳",
};

const baseValues = {
  common: 500,
  uncommon: 2000,
  rare: 8000,
  epic: 30000,
  legendary: 120000,
  mythic: 500000,
};

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function parsePlan(markdown) {
  const lines = markdown.split(/\r?\n/);
  const rows = [];
  let currentFacility = null;

  for (const line of lines) {
    const facilityMatch = line.match(/^###\s+\d+\.\d+.+`([a-z_]+)`/);
    if (facilityMatch) {
      currentFacility = facilityMatch[1];
      continue;
    }

    if (!currentFacility || !line.startsWith("|")) {
      continue;
    }

    const cols = line.split("|").map((col) => col.trim()).filter(Boolean);
    if (cols.length < 4 || cols[0] === "Rarity" || cols[0].startsWith("--------")) {
      continue;
    }

    const rarity = cols[0].toLowerCase();
    const resourceId = cols[1].replaceAll("`", "");
    if (!resourceId.startsWith("res_")) {
      continue;
    }

    const basePrice = baseValues[rarity] ?? 500;

    rows.push({
      id: resourceId,
      name: cols[2],
      name_tr: cols[3],
      type: "material",
      description: `${cols[3]} (${rarity})`,
      icon: facilityIcons[currentFacility] ?? "📦",
      rarity,
      base_price: basePrice,
      vendor_sell_price: Math.floor(basePrice * 0.35),
      is_tradeable: true,
      is_stackable: true,
      max_stack: 999,
      required_level: 1,
      can_enhance: false,
      material_type: "resource",
      production_building_type: currentFacility,
      facility_type: currentFacility,
      shop_available: false,
      shop_currency: "gold",
    });
  }

  return rows;
}

function toSql(rows) {
  const columns = [
    "id",
    "name",
    "type",
    "description",
    "icon",
    "rarity",
    "base_price",
    "vendor_sell_price",
    "is_tradeable",
    "is_stackable",
    "max_stack",
    "required_level",
    "can_enhance",
    "material_type",
    "production_building_type",
    "facility_type",
    "name_tr",
    "shop_available",
    "shop_currency",
  ];

  const values = rows.map((row) => `(${[
    sqlString(row.id),
    sqlString(row.name),
    sqlString(row.type),
    sqlString(row.description),
    sqlString(row.icon),
    sqlString(row.rarity),
    row.base_price,
    row.vendor_sell_price,
    row.is_tradeable,
    row.is_stackable,
    row.max_stack,
    row.required_level,
    row.can_enhance,
    sqlString(row.material_type),
    sqlString(row.production_building_type),
    sqlString(row.facility_type),
    sqlString(row.name_tr),
    row.shop_available,
    sqlString(row.shop_currency),
  ].join(", ")})`).join(",\n");

  return `-- AUTO-GENERATED from PLAN_02_FACILITIES_RESOURCES.md\n-- Generated by scripts/generate-plan2-resource-seed.mjs\n\nINSERT INTO public.items (${columns.join(", ")})\nVALUES\n${values}\nON CONFLICT (id) DO UPDATE SET\n  name = EXCLUDED.name,\n  type = EXCLUDED.type,\n  description = EXCLUDED.description,\n  icon = EXCLUDED.icon,\n  rarity = EXCLUDED.rarity,\n  base_price = EXCLUDED.base_price,\n  vendor_sell_price = EXCLUDED.vendor_sell_price,\n  is_tradeable = EXCLUDED.is_tradeable,\n  is_stackable = EXCLUDED.is_stackable,\n  max_stack = EXCLUDED.max_stack,\n  required_level = EXCLUDED.required_level,\n  can_enhance = EXCLUDED.can_enhance,\n  material_type = EXCLUDED.material_type,\n  production_building_type = EXCLUDED.production_building_type,\n  facility_type = EXCLUDED.facility_type,\n  name_tr = EXCLUDED.name_tr,\n  shop_available = EXCLUDED.shop_available,\n  shop_currency = EXCLUDED.shop_currency;\n`;
}

function main() {
  if (!fs.existsSync(planPath)) {
    throw new Error(`PLAN file not found: ${planPath}`);
  }

  const markdown = fs.readFileSync(planPath, "utf8");
  const rows = parsePlan(markdown);
  if (rows.length !== 90) {
    throw new Error(`Expected 90 resources, got ${rows.length}`);
  }

  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(outPath, toSql(rows), "utf8");
  console.log(`Generated ${rows.length} resource rows -> ${path.relative(root, outPath)}`);
}

main();